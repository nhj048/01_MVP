/*/////////////////////////////////////////////////////
	함수명: rollImg
	기능: 이미지를 순서대로 넘겨서 fade효과를 준다.
*//////////////////////////////////////////////////////
	
function rollImg(){
	var froll = $(".rollArea>div").first();
	var nroll = froll.next();

	nroll.hide().addClass("active").fadeIn(1000,function(){
		$(".rollArea").append(froll);
		froll.removeClass("active");
	});// fadeIn //
		
}// rollImg 함수 ////////////////////
